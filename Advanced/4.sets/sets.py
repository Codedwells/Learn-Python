mySet = {1, 2, 3, 4, 5, 6}
mySecondSet = {1, 2, 4, 3, 2, 1}
myThirdSet = set()
myFourthSet = set("Hello")
lastSet = set([1, 2, 3, 3, 4])

print(mySet)  # {1, 2, 3, 4, 5, 6}
print(mySecondSet)  # {1, 2, 3, 4}
print(type(myThirdSet))  # <class 'set'>
print(myFourthSet)  # {'e', 'H', 'o', 'l'}
print(lastSet)  # {1, 2, 3, 4}